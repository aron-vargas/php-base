                <li>Loan discount points: These points are additional funds you pay the lender at closing to get a lower
                    interest rate on your mortgage. Typically, each point you pay for a 30-year loan lowers your
                    interest
                    rate by .125 of a percentage point. If the current interest rate on a no-point, 30-year mortgage is
                    7.75
                    percent, paying one point would lower the interest rate to 7.625. Each point is one percent of the
                    mortgage (for example, if your mortgage is $200,000, one point equals $2,000).</li>
                <li>Appraisal fee: This fee pays for the appraisal, which the lender uses to determine whether the value
                    of
                    the property secures the loan should you default. The home buyer usually pays this fee. It may
                    appear on
                    the settlement form as “POC,” or “paid outside closing.”</li>
                <li>Credit report fee: This covers the cost of the credit report, which the lender uses to determine
                    your
                    creditworthiness.</li>
                <li>Assumption fee: This fee is charged if you take over the payments on the seller’s existing loan. It
                    may
                    range from hundreds of dollars to one percent of the loan amount.</li>
                <li>Prepaid interest: You are charged interest when you borrow money from a lender, and you will pay
                    interest on the mortgage amount from the date of settlement to the beginning of the period covered
                    by
                    the first monthly mortgage payment. At closing, you may be required to pay in advance the interest
                    for
                    the period.</li>
                <li>Escrow accounts: Also called reserves, these accounts are required if your lender will be paying
                    your
                    homeowner’s insurance and property taxes. Your lender sets up the escrow account by adding the cost
                    of
                    the insurance and taxes to your monthly mortgage payments. It is kept in reserve until the bills are
                    due. The bills are sent directly to your lender, who makes the payments for you.</li>
            </ul>

