            <h3>Six-Month Adjustable-Rate Mortgage</h3>

            <p class="glossary_definition">This adjustable-rate mortgage (ARM) offers a low initial interest rate for
                the
                first six months with an interest rate that adjusts every six months thereafter. The rate caps per
                adjustment can be 1 percent or 2 percent; the lifetime adjustment caps can be 4 percent, 5 percent, or 6
                percent. This type of mortgage may be right for you if you anticipate a rapid increase in income over
                the
                first few years of your mortgage. That’s because it lets you maximize your purchasing power immediately.
                It
                may also be the right mortgage for you if you plan to live in your home for only a few years.</p>

            <p class="glossary_definition">The interest rate is tied to a published financial index. When comparing ARMs
                that have different indexes, look at how the index has performed recently. Your an approved lender can
                provide information on how to track a specific index and how to review a 15-year history of the index.
            </p>

            <p class="glossary_definition">Advantages:</p>

            <ul>
                <li>Maximizes your buying power immediately, especially if you expect your income to rise quickly in the
                    next few years.</li>
                <li>Lets you select an index that meets your financial needs.</li>
                <li>Easier to qualify for due to a low interest rate and a 1 or 2 percent annual rate cap.</li>
                <li>Some six-month ARMs let you convert to a fixed-rate loan at certain adjustment intervals. Ask your
                    Fannie Mae approved lender which of their six-month ARMs include this option. Your lender can also
                    provide further specifics about this mortgage option.</li>
            </ul>

            <p>Details:</p>

            <ul>
                <li>You can get a six-month ARM with a term of 10 to 30 years. Typically, they are 10, 15, or 30 years.
                </li>
                <li>Can be used to buy one- to four-family, owner-occupied principal residences including second homes,
                    investment properties, and condos, co-ops and planned unit developments.</li>
                <li>Manufactured homes are also eligible. (Manufactured housing units must be built on a permanent
                    chassis
                    at a factory and then transported to a permanent site and attached to a foundation.)</li>
            </ul>

