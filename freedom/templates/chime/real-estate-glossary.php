            <h2><a name="_T">T</a></h2>

            <h3>Taxes and Insurance</h3>

            <p class="glossary_definition">You’ll hear many terms as you work with your mortgage lender, and one of the
                most
                frequently mentioned is “PITI.” This abbreviation stands for principal, interest, taxes and insurance.
            </p>

            <p class="glossary_definition">The tax and insurance components of a mortgage payment are generally held by
                the
                lender in an escrow account. The lender pays any property tax and homeowner’s insurance bills as they
                are
                due, ensuring they are paid on time.</p>

            <p class="glossary_definition">A home buyer’s monthly mortgage payment generally covers expenses through the
                escrow account. If you don’t have your homeowner’s insurance and property taxes paid out of a lender
                escrow
                account, your local government and your property insurance company will send payment notices directly to
                you. It is your responsibility to make sure you pay these bills on time.</p>

            <p class="glossary_definition">If you’re planning to purchase a condominium or cooperative, talk to your
                lender
                about how they view condo and co-op fees. Most likely, they are considered housing costs and not a part
                of
                PITI. However, this can vary from lender to lender.</p>

            <h3>Tenancy by the Entirety</h3>

            <p class="glossary_definition">A type of joint tenancy of property that provides right of survivorship and
                is
                available only to a husband and wife. Contrast with tenancy in common.</p>

            <h3>Tenancy in Common</h3>

            <p class="glossary_definition">A type of joint tenancy in a property without right of survivorship. Contrast
                with tenancy by the entirety and with joint tenacy.</p>

            <h3>Tenant-Stockholder</h3>

            <p class="glossary_definition">The obligee for a cooperative share loan, who is both a stockholder in a
                cooperative corporation and a tenant of the unit under a proprietary lease or occupancy agreement.</p>

            <h3>Termite Inspection</h3>

            <p class="glossary_definition">Homes in many parts of the country must be inspected for termites before they
                can
                be sold. You should receive a certificate from a termite inspection firm stating that the property is
                free
                of both visible termite infestation and termite damage.</p>

            <p class="glossary_definition">The cost of the termite inspection is usually paid by the seller, and the
                seller’s real estate sales professional orders the inspection. You need to make sure that the original
                certificate is delivered to your lender at least three days before closing.</p>

            <p class="glossary_definition">This allows the lender to review the certificate and address any potential
                problems.</p>

            <h3>Third-Party Origination</h3>

            <p class="glossary_definition">A process by which a lender uses another party to completely or partially
                originate, process, underwrite, close, fund, or package the mortgages it plans to deliver to the
                secondary
                mortgage market.</p>

            <h3>Thrifts</h3>

            <p class="glossary_definition">Thrifts are depository institutions that primarily serve consumers and
                include
                both savings banks and savings and loan (S&amp;L) institutions. These institutions originate and service
                mortgage loans. A thrift may choose to hold a loan in its own portfolio or sell the loan to an investor.
            </p>

            <h3>Title</h3>

            <p class="glossary_definition">A legal document evidencing a person’s right to or ownership of a property.
            </p>

            <h3>Title Company</h3>

            <p class="glossary_definition">A company that specializes in examining and insuring titles to real estate.
            </p>

            <h3>Title Insurance</h3>

            <p class="glossary_definition">Insurance that protects the lender (lender’s policy) or the buyer (owner’s
                policy) against loss arising from disputes over ownership of a property.</p>

            <p class="glossary_definition">Your lender will require that you buy title insurance to ensure that you are
                receiving a “marketable title”. There are two types of title insurance policies:</p>

            <ul>
                <li>Lender’s policy (mandatory): This protects the lender should a flaw in the title be detected after
                    the
                    property has been purchased.</li>
                <li>Owner’s policy (optional, but recommended): This protects you should a flaw in the title be detected
                    after the property has been purchased.</li>
            </ul>

            <p class="glossary_definition">Generally, the buyer pays the cost of both policies. Check with your insurer,
                because you may receive a price break if you seek a combined lender/owner policy or if you purchase a
                “reissue” policy from the company that previously insured the title.</p>

            <h3>Title Search</h3>

            <p class="glossary_definition">A check of the title records to ensure that the seller is the legal owner of
                the
                property and that there are no liens or other claims outstanding.</p>

            <p class="glossary_definition">In order to make sure the borrower will receive clear title to the property,
                lenders require a title search. It attempts to uncover any “encumbrances” on the title and makes sure
                the
                seller is the actual owner of the property.</p>

            <p class="glossary_definition">Encumbrances include any liens - legal claims against a property filed by
                creditors as a means to collect unpaid bills. Liens can also be filed by the Internal Revenue Service
                for
                nonpayment of taxes. Any such claims must be paid by the seller - this often occurs either before or at
                the
                closing.</p>

            <h3>Total Expense Ratio</h3>

            <p class="glossary_definition">Total obligations as a percentage of gross monthly income. The total expense
                ratio includes monthly housing expenses plus other monthly debts.</p>

            <h3>Townhouse</h3>

            <p class="glossary_definition">A townhouse is similar to a condominium in that it’s a type of joint real
                estate
                where each housing unit is individually owned. However, it has two or more stories, rather than the
                typical
                one floor found in a condominium.</p>

            <p class="glossary_definition">Townhouses are available in many shapes and sizes, and most may have yards or
                common spaces that can be used by the owners.</p>

            <h3>Trade Equity</h3>

            <p class="glossary_definition">Equity that results from a property purchaser giving his or her existing
                property
                (or an asset other than real estate) as trade as all or part of the down payment for the property that
                is
                being purchased.</p>

            <h3>Transfer of Ownership</h3>

            <p class="glossary_definition">Any means by which the ownership of a property changes hands. Lenders
                consider
                all of the following situations to be a transfer of ownership: the purchase of a property “subject to”
                the
                mortgage, the assumption of the mortgage debt by the property purchaser, and any exchange of possession
                of
                the property under a land sales contract or any other land trust device. In cases in which an inter
                vivos
                revocable trust is the borrower, lenders also consider any transfer of a beneficial interest in the
                trust to
                be a transfer of ownership.</p>

            <h3>Transfer Tax</h3>

            <p class="glossary_definition">State or local tax payable when title passes from one owner to another.</p>

            <h3>Treasury Index</h3>

            <p class="glossary_definition">An index that is used to determine interest rate changes for certain
                adjustable-rate mortgage (ARM) plans. It is based on the results of auctions that the U.S. Treasury
                holds
                for its Treasury bills and securities or is derived from the U.S. Treasury’s daily yield curve, which is
                based on the closing market bid yields on actively traded Treasury securities in the over-the-counter
                market.</p>

            <h3>Trustee</h3>

            <p class="glossary_definition">A fiduciary who holds or controls property for the benefit of another.</p>

            <h3>Truth-In-Lending</h3>

            <p class="glossary_definition">A federal law that requires lenders to fully disclose, in writing, the terms
                and
                conditions of a mortgage, including the annual percentage rate (APR) and other charges.</p>

            <p class="glossary_definition">Your lender should provide you with the Truth-in-Lending (TIL) Statement
                within
                three business days of your loan application. This document outlines the costs of your loan, and it is
                given
                to you so you can compare the costs with those of other lenders. Among the costs listed:</p>

            <ul>
                <li>The annual percentage rate (APR), which is the cost of your mortgage compiled as a yearly rate. It
                    may
                    be higher than the interest rate stated in your mortgage because it includes points and other costs
                    of
                    credit.</li>
                <li>The finance charge.</li>
                <li>The amount financed.</li>
                <li>The payment amount.</li>
                <li>The total payments required.</li>
            </ul>

            <p class="glossary_definition">The lender is required to give you the final version of your TIL Statement at
                or
                prior to the closing meeting because it is possible that the APR calculated at your loan application
                will
                change at closing.</p>

            <h3>Two-Step Mortgage</h3>

            <p class="glossary_definition">The Two-Step Mortgage is a special type of adjustable-rate mortgage (ARM)
                that
                adjusts only once. Depending on whether you select a five-year or seven-year Two-Step Mortgage, your
                interest rate will adjust once at the end of either five or seven years. Then, your interest rate stays
                the
                same for the remaining 25 or 23 years of your 30-year loan.</p>

            <p class="glossary_definition">Advantages:</p>

            <ul>
                <li>You can qualify with a low starting interest rate. Your initial interest rate is only slightly
                    higher
                    than a balloon loan and is often lower than a 30-year fixed rate loan.</li>
                <li>You get stable, predictable payments for five or seven years and, after adjustment, for the
                    remaining 25
                    or 23 years of the loan.</li>
                <li>You are protected from rising interest rates during the early years of homeownership.</li>
                <li>You do not have to re-qualify or pay refinance costs at the time the interest rate adjusts.</li>
                <li>You have time to increase your earnings or accumulate additional assets before the interest rate
                    adjusts
                    at the end of five or seven years.</li>
            </ul>

