            <h3>Fixed-Period Adjustable-Rate Mortgages</h3>

            <p class="glossary_definition">This type of adjustable-rate mortgage (ARM) maintains the same initial
                interest
                rate for the first three, five, seven, or 10 years of your loan, depending on the term you choose. Your
                interest rate then adjusts annually, and can move up or down as market conditions change. Be sure to ask
                your lender about the interest rate caps for both the annual adjustments and for the life of the loan.
            </p>

            <p class="glossary_definition">Advantages:</p>

            <ul>
                <li>Your initial interest rate will be lower than a fixed-rate mortgage, so you may be able to afford
                    more
                    home.</li>
                <li>You are protected against interest rate increases for the first three, five, seven, or 10 years of
                    the
                    loan, depending on which type of fixed-period ARM you choose.</li>
                <li>You may have the option to convert your ARM to a fixed-rate mortgage at the first, second, or third
                    interest rate adjustment dates.</li>
                <li>You have time to improve your financial position (i.e. salary increases) or accumulate additional
                    assets
                    before the interest rate adjusts at the end of the fixed period.</li>
            </ul>

            <p class="glossary_definition">Details:</p>

            <ul>
                <li>The lifetime interest rate cap for fixed-period ARMs is typically 5 to 6 percentage points above
                    your
                    initial rate. Your annual cap during the adjustable period is typically 1 to 2 percentage points
                    above
                    or below over the current rate.</li>
                <li>Can be used to buy one- to four-family residences including second homes and condos, co-ops and
                    planned
                    unit developments. Manufactured homes are also eligible. (Manufactured housing units must be built
                    on a
                    permanent chassis at a factory and then transported to a permanent site and attached to a
                    foundation.)
                </li>
            </ul>

