                will
                remain the same for as long as you have your loan. If you expect to live in your home for many years,
                having
                the same interest rate may be your key concern. If you decide that you like the stable, predictable
                payments
                of a fixed-rate loan, you have the option of choosing from a variety of repayment terms: 15, 20, and 30
                years are the most common. Typically, the longer the term of the mortgage, the more interest you pay
                over
                the life of your loan. However, stretching out your repayment term means your monthly mortgage payments
                will
                be less than they would be with a comparable shorter-term mortgage. Lenders offer a wide array of
                fixed-rate
                mortgages:</p>

            <ul>
                <li>Balloon Mortgages</li>
                <li>Biweekly Mortgages</li>
            </ul>

