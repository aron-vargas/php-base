            <h2><a name="_C">C</a></h2>

            <h3>Call Option</h3>

            <p class="glossary_definition">A provision in the mortgage that gives the mortgagee the right to call the
                mortgage due and payable at the end of a specified period for whatever reason.</p>

            <h3>Cap</h3>

            <p class="glossary_definition">A provision of an adjustable-rate mortgage (ARM) that limits how much the
                interest rate or mortgage payments may increase or decrease.</p>

            <p class="glossary_notes">*See also “lifetime payment cap”, “lifetime rate cap”, “periodic payment cap”, and
                “periodic rate cap”.</p>

            <h3>Capacity</h3>

            <p class="glossary_definition">Lenders will want to know if you can repay the mortgage debt you incur --
                this is
                known as your capacity. Lenders will base their evaluation on employment information, how long you’ve
                worked, and how much you are paid. Lenders will also review your expenses and any other debt obligations
                you
                have. This means they’ll want to know how many dependents you have and whether you pay any alimony or
                child
                support, for example.</p>

            <h3>Capital</h3>

            <p class="glossary_definition">&nbsp;</p>

            <ol>
                <li>Money used to create income, either as an investment in a business or an income property.</li>
                <li>The money or property comprising the wealth owned or used by a person or business enterprise.</li>
                <li>The accumulated wealth of a person or business.</li>
                <li>The net worth of a business represented by the amount by which its assets exceed liabilities.</li>
            </ol>

            <p>&nbsp;</p>

            <h3>Capital Expenditure</h3>

            <p class="glossary_definition">The cost of an improvement made to extend the useful life of a property or to
                add
                to its value.</p>

            <h3>Capital Improvement</h3>

            <p class="glossary_definition">Any structure or component erected as a permanent improvement to real
                property
                that adds to its value and useful life.</p>

            <h3>Cash-Out Refinance</h3>

            <p class="glossary_definition">A refinance transaction in which the amount of money received from the new
                loan
                exceeds the total of the money needed to repay the existing first mortgage, closing costs, points, and
                the
                amount required to satisfy any outstanding subordinate mortgage liens. In other words, a refinance
                transaction in which the borrower receives additional cash that can be used for any purpose.</p>

            <h3>CD-Indexed (Certificate of Deposit) ARMs</h3>

            <p class="glossary_definition">The Certificate of Deposit index represents the weekly average of secondary
                market interest rates on six-month negotiable CDs.</p>

            <p class="glossary_definition">The initial interest rate and payments adjust every six months after an
                initial
                six-month period.</p>

            <p class="glossary_definition">ARMs with this index typically come with a per-adjustment cap of 1 percent
                and a
                lifetime rate cap of 6 percent.</p>

            <h3>Certificate of Deposit</h3>

            <p class="glossary_definition">A document written by a bank or other financial institution that is evidence
                of a
                deposit, with the issuer’s promise to return the deposit plus earnings at a specified interest rate
                within a
                specified time period.</p>

            <p class="glossary_notes">*See also “Adjustable-Rate Mortgage” entry.</p>

            <h3>Certificate of Deposit Index</h3>

            <p class="glossary_definition">An index that is used to determine interest rate changes for certain ARM
                plans.
                It represents the weekly average of secondary market interest rates on six-month negotiable certificates
                of
                deposit.</p>

            <p class="glossary_notes">*See also “Adjustable-Rate Mortgage” entry.</p>

            <h3>Certificate of Eligibility</h3>

            <p class="glossary_definition">A document issued by the federal government certifying a veteran’s
                eligibility
                for a Department of Veterans Affairs (VA) mortgage.</p>

            <h3>Certificate of Reasonable Value (CRV)</h3>

            <p class="glossary_definition">A document issued by the Department of Veterans Affairs (VA) that establishes
                the
                maximum value and loan amount for a VA mortgage.</p>

            <h3>Certificate of Title</h3>

            <p class="glossary_definition">A statement provided by an abstract company, title company, or attorney
                stating
                that the title to real estate is legally held by the current owner.</p>

            <h3>Chain of Title</h3>

            <p class="glossary_definition">The history of all of the documents that transfer title to a parcel of real
                property, starting with the earliest existing document and ending with the most recent.</p>

            <h3>Change Frequency</h3>

            <p class="glossary_definition">The frequency (in months) of payment and/or interest rate changes in an
                adjustable-rate mortgage (ARM).</p>

            <h3>Change Orders</h3>

            <p class="glossary_definition">After construction begins, you may discover that you need to make unplanned
                and
                necessary changes to the work. The contingency reserve covers unforeseen repairs or deficiencies found
                during renovation. Unnecessary additions or changes are treated differently.</p>

            <p class="glossary_definition">These change orders are considered discretionary and must first be approved
                by
                your lender. You must deposit additional funds to pay for the work in the escrow account before work on
                the
                changes begins. These change orders - as well as any that result from unforeseen repairs - must be added
                as
                amendments to your construction contract.</p>

            <h3>Chattel</h3>

            <p class="glossary_definition">Another name for personal property.</p>

            <h3>Clear Title</h3>

            <p class="glossary_definition">A title that is free of liens or legal questions as to ownership of the
                property.
            </p>

            <h3>Closing</h3>

            <p class="glossary_definition">A meeting at which a sale of a property is finalized by the buyer signing the
                mortgage documents and paying closing costs. Also called “settlement”.</p>

            <p class="glossary_notes">*See also “Settlement” entry.</p>

            <h3>Closing Agent</h3>

            <p class="glossary_definition">As a potential home buyer, you will need a closing (or “settlement”) agent to
                coordinate the various closing activities. These can include but are not limited to preparing and
                recording
                the closing documents and disbursing funds.</p>

            <p class="glossary_definition">The types of services provided by a closing agent depend on the person you
                hire,
                but typically the closing is conducted by title companies, escrow companies or attorneys. It is usually
                held
                at the lender’s or real estate sales professional’s office.</p>

            <h3>Closing Cost Item</h3>

            <p class="glossary_definition">A fee or amount that a home buyer must pay at closing for a single service,
                tax,
                or product. Closing costs are made up of individual closing cost items such as origination fees and
                attorney’s fees. Many closing cost items are included as numbered items on the HUD-1 statement.</p>

            <h3>Closing Costs</h3>

            <p class="glossary_definition">Expenses (over and above the price of the property) incurred by buyers and
                sellers in transferring ownership of a property. Closing costs normally include an origination fee, an
                attorney’s fee, taxes, an amount placed in escrow, and charges for obtaining title insurance and a
                survey.
                Closing costs percentage will vary according to the area of the country; lenders or REALTORS® often
                provide
                estimates of closing costs to prospective homebuyers.</p>

            <h3>Closing Date</h3>

            <p class="glossary_definition">After your lender has approved your mortgage and you accept the commitment
                letter, the next step is to set a closing date. Many times, your real estate sales professional
                coordinates
                the setting of this date with you, the seller, the closing agent, and your lender.</p>

            <p class="glossary_definition">Remember, you need to ensure that the closing occurs before your lender’s
                commitment letter - and the rate lock-in, if there is one - expire. You can now finalize your moving
                plans.
            </p>

            <h3>Cloud on Title</h3>

            <p class="glossary_definition">Any conditions revealed by a title search that adversely affect the title to
                real
                estate. Usually clouds on title cannot be removed except by a quitclaim deed, release, or court action.
            </p>

            <h3>Co-Maker</h3>

            <p class="glossary_definition">A person who signs a promissory note along with the borrower. A co-maker’s
                signature guarantees that the loan will be repaid, because the borrower and the co-maker are equally
                responsible for the repayment.</p>

            <h3>Coinsurance</h3>

            <p class="glossary_definition">A sharing of insurance risk between the insurer and the insured. Coinsurance
                depends on the relationship between the amount of the policy and a specified percentage of the actual
                value
                of the property insured at the time of the loss.</p>

            <h3>Coinsurance Clause</h3>

            <p class="glossary_definition">A provision in a hazard insurance policy that states the amount of coverage
                that
                must be maintained - as a percentage of the total value of the property - for the insured to collect the
                full amount of a loss.</p>

            <h3>Collateral</h3>

            <p class="glossary_definition">An asset (such as a car or a home) that guarantees the repayment of a loan.
                The
                borrower risks losing the asset if the loan is not repaid according to the terms of the loan contract.
            </p>

            <h3>Collection</h3>

            <p class="glossary_definition">The efforts used to bring a delinquent mortgage current and to file the
                necessary
                notices to proceed with foreclosure when necessary.</p>

            <h3>Commercial Banks</h3>

            <p class="glossary_definition">Commercial banks, like thrifts, originate and service mortgage loans. In some
                cases, commercial banks may have mortgage banking subsidiaries that perform this function. Banks may
                choose
                to hold a loan in their own portfolio or sell the loan to an investor.</p>

            <h3>Commission</h3>

            <p class="glossary_definition">The fee charged by a broker or agent for negotiating a real estate or loan
                transaction. A commission is generally a percentage of the price of the property or loan.</p>

            <h3>Commitment Letter</h3>

            <p class="glossary_definition">A formal offer by a lender stating the terms under which it agrees to lend
                money
                to a homebuyer. Also known as a “loan commitment”.</p>

            <h3>Common Area Assessments</h3>

            <p class="glossary_definition">Levies against individual unit owners in a condominium or planned unit
                development (PUD) project for additional capital to defray homeowners’ association costs and expenses
                and to
                repair, replace, maintain, improve, or operate the common areas of the project.</p>

            <h3>Common Areas</h3>

            <p class="glossary_definition">Those portions of a building, land, and amenities owned (or managed) by a
                planned
                unit development (PUD) or condominium project’s homeowners’ association (or a cooperative project’s
                cooperative corporation) that are used by all of the unit owners, who share in the common expenses of
                their
                operation and maintenance. Common areas include swimming pools, tennis courts, and other recreational
                facilities, as well as common corridors of buildings, parking areas, means of ingress and egress, etc.
            </p>

            <h3>Common Law</h3>

