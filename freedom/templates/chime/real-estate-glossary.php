                wind, vandalism, or other hazards.</p>

            <h3>Home Equity Conversion Mortgage (HECM)</h3>

            <p class="glossary_definition">A special type of mortgage that enables older home owners to convert the
                equity
                they have in their homes into cash, using a variety of payment options to address their specific
                financial
                needs. Unlike traditional home equity loans, a borrower does not qualify on the basis of income but on
                the
                value of his or her home. In addition, the loan does not have to be repaid until the borrower no longer
                occupies the property. Sometimes called a reverse mortgage.</p>

            <p class="glossary_definition">A Home Equity Conversion Mortgage (HECM) is a type of home loan that lets
                homeowners aged 62 or over with little or no remaining balance on their mortgage convert their equity
                into
                cash. The equity can be paid to the homeowner in a lump sum, in a stream of payments, draws from a line
                of
                credit, or a combination of monthly payments and line of credit.</p>

            <p class="glossary_definition">Whatever payment plan you select, you do not have to repay any part of this
                reverse mortgage until you sell the home or vacate it for another reason. At that time, you pay the loan
                balance, plus any accrued interest. Any proceeds above that amount go to you or to your estate.</p>

            <p>Developed by the Federal Housing Administration (FHA), the HECM mortgage provides a cash growth feature
                not
                found with some other reverse mortgages - check with your Fannie Mae approved lender to see how this
                works
                based on your personal needs and your payment plan.</p>

            <p class="glossary_definition">Advantages:</p>

            <ul>
                <li>The funds are yours to spend in any way you choose.</li>
                <li>There are no monthly payments with a HECM.</li>
                <li>Your loan funds do not affect Social Security or Medicare benefits. (If you receive Supplemental
                    Social
                    Security or Medicaid, these benefits may be affected.)</li>
                <li>You do not have to pay back the loan until you sell your home or no longer use it for your primary
                    residence. Then, you or your estate will repay the cash you received from the HECM, plus interest
                    and
                    other finance charges to the lender. This means that the remaining equity in your home can be passed
                    on
                    to your heirs through the sale of the property.</li>
                <li>You will never owe more than the value of the home at the time of repayment, even if the loan
                    balance
                    exceeds the value of your property. This means no debt will ever be passed along to the estate or
                    your
                    heirs.</li>
            </ul>

            <p>Details:</p>

            <ul>
                <li>You and any co-borrowers must be at least 62 years old.</li>
                <li>You must own your home outright - or carry a small mortgage balance.</li>
                <li>Eligible properties include a single-family home, a two- to four-unit dwelling, a condominium or a
                    manufactured home. All housing types must meet Federal Housing Administration (FHA) guidelines. (Ask
                    your lender if your property qualifies.)</li>
                <li>Your home must be your principal residence, which means you must live in it more than half the year.
                </li>
                <li>You must attend pre-application mortgage counseling before you apply for the loan.</li>
                <li>You must keep applicable taxes current, as well as maintain insurance coverage on your home.</li>
                <li>The amount you can borrow with a HECM depends on the age of the youngest borrower(s), the interest
                    rate,
                    how much your house is worth, and the maximum claim amount. In general, you can get between
                    one-third
                    and one-half of your equity as a line of credit or as a lump sum payment.</li>
                <li>The balance of funds advanced against the equity in your home is due and payable when you relinquish
                    your home as a primary residence, or if the borrower(s) pass away. You may have to pay off the debt
                    if
                    you fail to pay property taxes or insurance or if you do not maintain your property.</li>
            </ul>

