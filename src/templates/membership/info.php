    <h3>HOW MUCH DOES A MEMBERSHIP COST?</h3>
    <p>Railside Kitchen charges an hourly rate for kitchen time, which varies based on your monthly Membership level.
        The minimum level of Membership we offer is the Basic Level,
        which allows for 20 hours of kitchen time during peak hours.
        There is an additional monthly rate for cold, freezer, and dry storage.
        We also offer parking for food trucks and trailers. We offer 6-month or 12-month Membership contracts.
        Check out our <a href="/membership/pricing">pricing page</a> for pricing and other details.</p>
    <h3>Does Railside Kitchen Have ______?</h3>
    <p>Most likely! Check out our equipment list to see some of the equipment we have available for Members.
        We also have a large amount of smallwares: sheet pans, hotel pans, bus tubs, cambros, spatulas,
        spoons, scales, etc. Weekly linen/towel service is provided for use in the kitchen.
        Additional towels can be arranged for off-site service, such as for food trucks. For most applications,
        if you bring your knife kit and a sharpie, you'll be set.</p>
    <p>If your business is dependent on specialty equipment that we do not currently have on site;
        feel free to reach out to see if we can accomodate your equipment, have the space to store it,
        and what the monthly storage costs would be.
        You may bring in your own specialty smallwares to store on your rental storage racks.
        Storage rates are à la carte and not included in monthly membership costs.</p>
    <h3>I NEED ACCESS TO YOUR KITCHEN FOR AN EVENT. CAN I RENT TIME IN YOUR KITCHEN?</h3>
    <p>Depending on your requirements, we can allow one-time use rentals at Kitchen Collab for Occasional-Use Clients.
        We will prioritize with scheduling for our current Members who have committed to monthly usage in our
        shared kitchen and have a health permit to operate out of our facility.
        However, if you are an established business with an existing health permit, insurance, and are
        Food Safety Manager certified; then we can begin to discuss our policies towards Occasional-Use
        Clients and see if our current kitchen availability will meet your needs.
        Occasional-Use Clients will be required to provide the same documents as regular Members.
        The hourly rate for kitchen time for Occasional-Use Clients is $38/hr.
        Occasional-Use Clients only have access to the kitchen during typical business hours.</p>
